{
  const char *defaultstr = ((struct FC_Type *)fcstr)->Str;

  if(LocaleBase)
    return (const char *)GetCatalogStr(NL_Catalog, ((struct FC_Type *)fcstr)->ID, (STRPTR)defaultstr);

  return defaultstr;
}
///
/// CloseCat()
void CloseCat(void)
{
  ENTER();

  if(NL_Catalog)
  {
    CloseCatalog(NL_Catalog);
    NL_Catalog = NULL;
  }

  LEAVE();
}
